# BASIC OPERATIONS

# Addition +
print(10 + 5)  # 15

# Substraction -
print(10 - 5)  # 5

# Multiplication *
print(11 * 5)  # 55

# Division (always float) /
print(5 / 2)  # 2.5

# Floor Division (no decimals) //
print(5 // 2)  # 2

# Modulo (remainder) %
print(5 % 2)  # 1

# Exponentiation (power) **
print( 3**4) # 81


